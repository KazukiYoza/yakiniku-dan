                            <h4 class="difference-title">国産牛</h4>
                            <p class="difference-text">
                                原産国、原産地、品種に関わらず、日本で飼育された期間が最も長く、
                                日本国内で食肉用に加工された牛（精肉）を「国産牛」として表示されています。
                            </p>
                        </li>
